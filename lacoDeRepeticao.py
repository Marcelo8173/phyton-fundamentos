for n1 in range(1,6):
    print(n1)

# colocando um step
for n2 in range(5,0,-1):
    print(n2)

# for com soma
print("\nSoma")
soma = 0
for n3 in range(0,6):
    soma = soma + n3
print(soma)

# string
print("\nString")
palavra = "marcelo"
for letra in palavra:
    print(letra)

# while
print("\nWhile")
numero = 1
while numero < 6:
    print(numero)
    numero += 1

soma = 0
n1 = 1
while n1 < 6:
    soma += n1
    n1 += 1
    print(soma)